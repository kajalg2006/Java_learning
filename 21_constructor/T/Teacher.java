public class Teacher {
    String designation;
    int experience;
    float salary;
    String name;
    int age;
    String phone;

    public Teacher(String designation, int experience, float salary, String name, int age, String phone) {
        this.designation = designation;
        this.experience = experience;
        this.salary = salary;
        this.name = name;
        this.age = age;
        this.phone = phone;
    }
}
