class Student{

    
    String college;    
    String degree;    
    String branch;    
    String name;
    int age;
    String phone;


    public Student(String college, String degree, String branch, String name, int age, String phone ){
        this.college = college;
        this.degree = degree;
        this.branch = branch;
        this.name = name;
        this.age = age;
        this.phone = phone;
    }

}